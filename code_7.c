/* code_7.c
Считаем объем при выводе в printf
*/
#include <stdio.h>
#include <locale.h>

int main(void)
{
	setlocale(LC_ALL, "rus");

	int dlina = 10, shirina = 11, visota = 12;

	printf("Длина = %d, Ширина = %d  Высота = %d Объем = %d", dlina, shirina, visota, dlina * shirina * visota);

	return 0;
}
